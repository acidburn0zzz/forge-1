    #  						Server Reconfiguration
    #  						SOLICIT -->
    #  Make at least				<--	ADVERTISE
    #   one leases			REQUEST -->
    #  								<--	REPLY
    #  with saved IA_NA		CONFIRM -->
    #  NotOnLink error	  		    <--	REPLY
    #  Pass Criteria:
    #  				REPLY MUST include option:
    # 					client-id
    # 					server-id
    # 					status-code with code 4
